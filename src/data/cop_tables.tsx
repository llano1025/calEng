// Define types for better structure and type checking
export type VoltageDropValues = { r?: number; x?: number; z?: number; dc?: number };
export type SizeTable = { [size: string]: number };
export type VoltageDropTable = { [size: string]: VoltageDropValues };

// Type definitions for consistent structure
type BasicMethodTable = {
  '2'?: SizeTable;
  '3'?: SizeTable;
  '3_4'?: SizeTable;
  [key: string]: SizeTable | undefined;
};

type TouchingConfig = {
  '2'?: SizeTable;
  '3'?: SizeTable;
  'flat'?: SizeTable;
  'flat_3ph'?: SizeTable;
  'trefoil'?: SizeTable;
  '3_flat'?: SizeTable;
  '3_trefoil'?: SizeTable;
  'cables_touching'?: SizeTable;
  [key: string]: SizeTable | undefined;
};

type SpacedConfig = {
  'horizontal'?: SizeTable;
  'vertical'?: SizeTable;
  'dc_horizontal'?: SizeTable;
  'dc_vertical'?: SizeTable;
  'ac_2_horizontal'?: SizeTable;
  'ac_2_vertical'?: SizeTable;
  'ac_3_horizontal'?: SizeTable;
  'ac_3_vertical'?: SizeTable;
  [key: string]: SizeTable | undefined;
};

type MethodFConfig = {
  touching: TouchingConfig;
  spaced: SpacedConfig;
  [key: string]: TouchingConfig | SpacedConfig | any;
};

type CccData = {
  methodA?: BasicMethodTable;
  methodB?: BasicMethodTable;
  methodC?: BasicMethodTable;
  methodE?: BasicMethodTable;
  methodF?: MethodFConfig | { [key: string]: SizeTable };
  methodG?: { [key: string]: SizeTable };
  [key: string]: any;
};

type ACVoltageDropData = {
  methodAB?: { [key: string]: VoltageDropTable };
  methodB?: { [key: string]: VoltageDropTable };
  methodC_touching?: { [key: string]: VoltageDropTable };
  methodCF?: {
    touching?: { [key: string]: VoltageDropTable };
    spaced?: { [key: string]: VoltageDropTable };
    [key: string]: { [key: string]: VoltageDropTable } | undefined;
  };
  methodCFG?: {  // Add this new property definition
    touching?: { [key: string]: VoltageDropTable };
    spaced?: { [key: string]: VoltageDropTable };
    [key: string]: { [key: string]: VoltageDropTable } | undefined;
  };
  methodF_trefoil?: { [key: string]: VoltageDropTable };
  methodF_spaced?: { [key: string]: VoltageDropTable };
  single_phase?: {
    touching?: VoltageDropTable;
    spaced?: VoltageDropTable;
    [key: string]: VoltageDropTable | undefined;
  };
  three_phase?: {
    trefoil?: VoltageDropTable;
    flat_touching?: VoltageDropTable;
    flat_spaced?: VoltageDropTable;
    [key: string]: VoltageDropTable | undefined;
  };
  '2'?: VoltageDropTable;
  '3'?: VoltageDropTable;
  '3_4'?: VoltageDropTable;
  [key: string]: any;
};

type VoltageDropData = {
  dc: VoltageDropTable;
  ac: ACVoltageDropData;
  [key: string]: VoltageDropTable | ACVoltageDropData | any;
};

interface CableData {
  ccc: CccData;
  voltageDrop: VoltageDropData;
  [key: string]: CccData | VoltageDropData | any;
}

interface ArmourData {
  singleCore: CableData;
  multiCore: CableData;
  [key: string]: CableData | any;
}

interface InsulationData {
  nonArmoured: ArmourData;
  armoured: ArmourData;
  [key: string]: ArmourData | any;
}

interface ConductorData {
  [insulation: string]: InsulationData;
}

